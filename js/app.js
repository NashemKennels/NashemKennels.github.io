var App = function(){
  var $homeImgs = $('.home')
  ,   $titles   = $('.home h1');

  this.homePage = function(){
    homePageAnimate();
  };

  this.dogPage = function(){
    initializeDogs();
  }

  this.lostDogPage = function(){
    initializeLostDogs();
  }

  var homePageAnimate = function(){
    setTimeout(fadeHomeImgs, 500);
    setTimeout(fadeHomeImgs, 1000);
  }

  var fadeHomeImgs = function(){
    $homeImgs.toggleClass('fade');
  }

  var toggleTitles = function(){
    $titles.toggleClass('visible');
  }

  var initializeDogs = function(){
    dogCollection = new DogCollection(); //creates empty dog collection

    _.each(nashemDogs, function(dog){ //populates collection with models created from object above
      if (!dog.died){
        var dogModel = new Dog(dog);
        dogCollection.add(dogModel);
      }
    });

    dogListView = new DogListView({collection: dogCollection, el: $('.dog-container') });
  };

  var initializeLostDogs = function(){
      dogCollection = new DogCollection(); //creates empty dog collection

      _.each(nashemDogs, function(dog){ //populates collection with models created from object above
        if (dog.died){
          var dogModel = new Dog(dog);
          dogCollection.add(dogModel);
        }
      });

      dogListView = new DogListView({collection: dogCollection, el: $('.dog-container') });
  };

};


